// while loop adalah jenis perulangan yang lebih sederhana dibandingkan perulangan for
// perulangan ini hanya ada kondisi saja, jika kondisinya true maka akan melakukan perulangan sebanyak yg ditentukan
// jika kondisinya false maka tidak akan dijalankan


/* <===============================================================================> */
/* <===============================================================================> */


// contoh penggunaan while
let i = 1;
while(i <= 10){
    console.log(`perulangan while ke-${i}`);
    i++;
    // melakukan perulangan sebanyak 10 kali
}


// perulangan while tanpa henti
while(true){
    console.log("perulangan while tanpa henti");
    // perulangan while tanpa henti
}

