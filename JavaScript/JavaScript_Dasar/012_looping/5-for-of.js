// for-of loop digunakan di JavaScript untuk mengulang melalui elemen-elemen dalam struktur data yang dapat diiterasi, seperti array, string, objek Map, objek Set, dan banyak lagi
// namun pada contoh kali ini yang akan kita gunakan adalah array dan string

/* <===============================================================================> */
/* <===============================================================================> */


// contoh penggunaan for-of loop pada string
let text = "Hello";
for(let char of text){
    console.log(char);
}


// contoh penggunaan for-of loop pada array
let person = ["Atyla", 18, "programming", "smkn 3 banjar"];
for(let biodata of person){
    console.log(biodata);
}

