// JavaScript mempunyai banyak sekali tipe data
// diantaranya sebagai berikut

/* 

1. String
2. Number
3. Boolean
4. Array
5. Object
6. Undefined
7. NaN (Not a Number)
8. Null

*/

// untuk mengecek data tipe datanya apa, kita bisa menggunakan kata kunci typeof

console.log(typeof "Hello World!"); // string
console.log(typeof 12345); //number
console.log(typeof true); // boolean
console.log(typeof ["halo"]); // object
console.log(typeof {nama: "Harits"}); // object
console.log(typeof undefined); // undefined
console.log(typeof NaN); // number
console.log(typeof null); // object
