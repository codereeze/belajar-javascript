// variable adalah tempat menampung data yang nantinya data tersebut bisa digunakan ke program selanjutnya
// variable ada yang bersifat mutable (nilai bisa berubah) dan imutable (tidak bisa berubah)
// di javascript ada 3 jenis variable yaitu var, let, dan const;

/* <===============================================================================> */
/* <===============================================================================> */


// membuat variable di JavaScript


// membuat variable tanpa menggunakan kata kunci
sayHello = "Hello World";
console.log(sayHello); // Hello World



// membuat variable dengan kata kunci var
var sayGoodNight = "selamat malam";
console.log(sayGoodNight); // selamat malam



// membuat variable dengan kata kunci let
let sayGoodMorning = "selamat pagi";
console.log(sayGoodMorning); // selamat pagi



// membuat variable dengan kata kunci const
const sayGoodAfternoon = "selamat siang";
console.log(sayGoodAfternoon); // good afternoon



/* <===============================================================================> */
/* <===============================================================================> */



// kita bisa membuat banyak variable dalam satu kata kunci saja
// yaitu dengan cara menggunakan , (tanda koma)
// contohnya


// var
var fullName = "Al Harits", address = "Lakbok, Ciamis", age = 18;
console.log(fullName + " " + address + " " + age); // Al Harits Lakbok, Ciamis 18


// let
let firstName = "Atyla", middleName = "Azfa Al", lastName = "Harits";
console.log(firstName + " " + middleName + " " + lastName); // Atyla Azfa Al Harits


// const
const langFavourite = "JavaScript", experience = "1 year", level = "Masih pemula";
console.log(langFavourite + " " + experience + " " + level); // JavaScript 1 year Masih pemula



