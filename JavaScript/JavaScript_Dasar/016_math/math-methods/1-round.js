// method round digunakan untuk membulatkan bilangan pecahan
// jika pecahannya kurang dari lima maka akan dibulatkan ke bawah
// jika pecahannya lebih dari atau sama dengan lima maka akan dibulatkan ke atas

/* <===============================================================================> */
/* <===============================================================================> */


// pembulatan ke bawah
let floor = Math.round(10.4);
console.log(floor); // 10

// pembulatan ke atas
let ceil = Math.round(10.5);
console.log(ceil); // 11

