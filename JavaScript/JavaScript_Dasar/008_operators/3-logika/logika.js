// operator logika adalah operator yang digunakan untuk melakukan operasi logika
/* 

( && ) => opreator ini digunakan untuk melakukan operasi logika "dan" antara dua ekspresi
hasilnya true jika kedua ekspresi bernilai true

( || ) => operator ini digunakan untuk melakukan operasi logika "atau" antara dua ekspresi
hasilnya true jika salah satu atau keduanya dari kedua ekspresi bernilai true

( ! ) => operator ini digunakan untuk melakukan operasi logika "kebalikan" 
digunakan untuk membalikan nilai boolean

*/

/* <===============================================================================> */
/* <===============================================================================> */


// logika "dan"
console.log(true && true); // true
console.log(true && false); // false

// logika "atau"
console.log(true || true); // true
console.log(true || false); // true
console.log(false || false); // false

// logika "kebalikan"
console.log(!false); // true
console.log(!true); // false
