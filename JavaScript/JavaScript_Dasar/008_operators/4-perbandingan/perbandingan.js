// operator perbandingan adalah operator yang digunakan untuk membandingan dua ekspresi
// hasil dari perbandingan tersebut adalah nilai boolean
/* 

( > ) => lebih dari
( < ) => kurang dari
( >= ) => lebih dari sama dengan
( <= ) => kurang dari sama dengan
( == ) => sama dengan
( === ) => sama dengan dan sama tipe data
( != ) => tidak sama dengan
( !== ) => tidak sama dengan dan tidak sama tipe

*/

/* <===============================================================================> */
/* <===============================================================================> */

console.log(10 > 9); // true
console.log(10 < 9); // false
console.log(10 >= 10); // true
console.log(11 <= 10); // false
console.log("10" == 10); // true
console.log("10" === 10); // false
console.log(10 != 11); // true
console.log(10 !== 10); // false
