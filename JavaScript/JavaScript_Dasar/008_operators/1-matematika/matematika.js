// operator matematika adalah operator yang digunakan untuk melakukan operasi bilangan angka
// seperti penjumlahan, pengurangan, perkalian, pembagian dan lain-lain.
// berikut merupakan operator matematika

/* 

( + ) => digunakan untuk penjumlahan
( - ) => digunakan untuk pengurangan
( * ) => digunakan untuk perkalian
( / ) => digunakan untuk pembagian
( % ) => digunakan untuk sisa bagi
( ** ) => digunakan untuk pemangkatan

*/

/* <===============================================================================> */
/* <===============================================================================> */

// penjumlahan
let penjumlahan = 10 + 10;
console.log(penjumlahan); 


// pengurangan
let pengurangan = 20 - 10;
console.log(pengurangan); 


// perkalian
let perkalian = 10 * 10;
console.log(perkalian); 


// pembagian
let pembagian = 20 / 10;
console.log(pembagian); 


// sisa bagi
let modulus = 5 % 3;
console.log(modulus); 


// pemangkatan
let pemangkatan = 2 ** 3;
console.log(pemangkatan); // 8 (dua pangkat tiga sama dengan 8) (2 x 2 x 2 = 8)

