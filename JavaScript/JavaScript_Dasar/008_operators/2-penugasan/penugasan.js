// selain digunakan untuk menugaskan variable
// kita juga menggunakan operator ini untuk melakukan operasi matematika
// yaitu dengan cara mengkombinasikan operator matematika dengan operator penugasan


// penjumlahan
let a = 10;
a += 10; // sama saja dengan a = a + 10; a sekarang bernilai 20
console.log(a); // 20



// pengurangan
let b = 12;
b -= 10; // sama saja dengan b = b - 10; b sekarang bernilai 2
console.log(b); // 2



// perkalian
let c = 10;
c *= 10; // sama saja dengan c = c * 10; c sekarang bernilai 100
console.log(c); // 100



// pembagian
let d = 10;
d /= 10; // sama saja dengan d = d / 10; d sekarang bernilai 1
console.log(d); // 1



// sisa bagi
let e = 5;
e %= 3; // sama saja dengan e = e % 3; e sekarang bernilai 2
console.log(e); // 2






