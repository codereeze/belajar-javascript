// method valueOf digunakan untuk mengembalikan nilai primitif yang sesuai dengan object yang sedang dipertimbangkan
// methon ini akan mengembalikan nilai primitif dalam bentuk angka

/* <===============================================================================> */
/* <===============================================================================> */

// primitif
let angka = 12345;
console.log(angka.valueOf()); // 12345


// object
let objectNumber = new Number(100);
console.log(objectNumber.valueOf()); // 100